class BenchmarkTrackerV2:
    def __init__(self, history_file="tests/scre_benchmark_history.json", report_file="BENCHMARK_REPORT.md"):
        self.history_file = history_file
        self.report_file = report_file

    def record_run(self, results: Dict[str, Any]):
        history = []
        if os.path.exists(self.history_file):
            with open(self.history_file, "r") as f:
                try:
                    history = json.load(f)
                except ValueError:
                    pass
        
        entry = {
            "timestamp": datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            "results": results
        }
        history.append(entry)

        with open(self.history_file, "w") as f:
            json.dump(history, f, indent=4)

        self._generate_markdown_report(history)

    def _generate_markdown_report(self, history: List[Dict]):
        v2_history = [item for item in history if "results" in item]
        if not v2_history: return
        latest = v2_history[-1]
        with open(self.report_file, "w") as f:
            f.write("# 📊 SCRE V2 Performance Benchmark Report\n\n")
            f.write("> **Auto-generated by `benchmark_v2.py`**\n>\n")
            f.write(f"**Latest Run:** {latest['timestamp']}\n\n")
            f.write("## 🚀 Latest Run Metrics\n\n")
            f.write("| Strategy | Exact Match | F1 Score | Semantic Sim | Answer Consistency | Latency (ms) | Compression | Cost Savings |\n")
            f.write("|---|---|---|---|---|---|---|---|\n")
            for strategy, metrics in latest["results"].items():
                name = strategy.split('_', 1)[-1].replace('_', ' ') if '_' in strategy else strategy
                f.write(f"| **{name}** | {metrics['exact_match']:.4f} | {metrics['f1_score']:.4f} | {metrics['semantic_similarity']:.4f} | {metrics['answer_consistency']:.4f} | {metrics['latency_ms']:.2f}ms | {metrics['compression_ratio']:.2%} | ${metrics['cost_savings_usd']:.4f} |\n")
            
            f.write("\n## 📈 Historical Trend (SCRE Accuracy)\n\n")
            f.write("| Date & Time | Exact Match | F1 Score | Semantic Sim | Compression Ratio |\n")
            f.write("|---|---|---|---|---|\n")
            for item in reversed(v2_history[-15:]):
                scre_metrics = item["results"].get("D_SCRE", {})
                if scre_metrics:
                    f.write(f"| {item['timestamp']} | {scre_metrics.get('exact_match', 0):.4f} | {scre_metrics.get('f1_score', 0):.4f} | {scre_metrics.get('semantic_similarity', 0):.4f} | {scre_metrics.get('compression_ratio', 0):.2%} |\n")

class BenchmarkFramework:
    def __init__(self, cost_per_1k_tokens: float = 0.0015, evaluation_model: str = "gemma4:e2b"):
        self.cost_per_1k_tokens = cost_per_1k_tokens
        self.evaluation_model = evaluation_model
        if SentenceTransformer:
            self.embedder = SentenceTransformer("all-MiniLM-L6-v2")
            self.similarity_model = SentenceTransformer("all-mpnet-base-v2") # Higher capacity for eval
        else:
            self.embedder = None
            self.similarity_model = None
            
        self.baseline_answers = {}
            
    def exact_match(self, prediction: str, truth: str) -> float:
        return 1.0 if truth.lower().strip() in prediction.lower().strip() else 0.0
        
    def f1_score(self, prediction: str, truth: str) -> float:
        pred_tokens = prediction.lower().split()
        truth_tokens = truth.lower().split()
        common = set(pred_tokens) & set(truth_tokens)
        if not common:
            return 0.0
        precision = len(common) / len(pred_tokens)
        recall = len(common) / len(truth_tokens)
        return 2 * (precision * recall) / (precision + recall)
        
    def semantic_similarity(self, prediction: str, truth: str) -> float:
        if not self.similarity_model or not prediction.strip() or not truth.strip():
            return 0.0
        emb1 = self.similarity_model.encode(prediction, convert_to_tensor=True)
        emb2 = self.similarity_model.encode(truth, convert_to_tensor=True)
        return float(util.cos_sim(emb1, emb2)[0][0])
        
    def generate_answer(self, context: str, query: str) -> str:
        if not ollama: return "Ollama not installed."
        prompt = f"Answer using ONLY the context. If unknown, say 'Unknown'.\n\nContext:\n{context}\n\nQuestion:\n{query}"
        try:
            response = ollama.chat(model=self.evaluation_model, messages=[{"role": "user", "content": prompt}])
            return response["message"]["content"].strip()
        except Exception as e:
            print(f" [LLM Error: {e}]", end="")
            return "Error"

    def evaluate_strategy(self, strategy_name: str, retrieval_func, dataset: List[Dict]) -> Dict[str, Any]:
        print(f"\n🚀 Evaluating Strategy: {strategy_name}...")
        metrics = {
            "exact_match": 0.0,
            "f1_score": 0.0,
            "semantic_similarity": 0.0,
            "latency_ms": 0.0,
            "compression_ratio": 0.0,
            "cost_savings_usd": 0.0,
            "context_size_tokens": 0.0,
            "answer_consistency": 0.0
        }
        
        is_baseline = (strategy_name == "Raw")
        
        for item in dataset:
            doc_id = item.get("id", hash(item["question"]))
            raw_context = item["context"]
            query = item["question"]
            truth = item["expected_answer"]
            
            print(f"  -> Q: '{query[:50]}...' ", end="", flush=True)
            start_time = time.time()
            reduced_context = retrieval_func(raw_context, query)
            latency = (time.time() - start_time) * 1000
            
            # Token estimation
            raw_tokens = max(1, len(raw_context.split()) * 1.3)
            reduced_tokens = len(reduced_context.split()) * 1.3
            
            # Real LLM inference
            inference_start = time.time()
            llm_answer = self.generate_answer(reduced_context, query)
            inference_time = time.time() - inference_start
            print(f"(Reduction: {latency:.0f}ms, LLM: {inference_time:.1f}s)")
            
            if is_baseline:
                self.baseline_answers[doc_id] = llm_answer
            
            metrics["exact_match"] += self.exact_match(llm_answer, truth)
            metrics["f1_score"] += self.f1_score(llm_answer, truth)
            metrics["semantic_similarity"] += self.semantic_similarity(llm_answer, truth)
            metrics["latency_ms"] += latency
            metrics["compression_ratio"] += (1 - (reduced_tokens / raw_tokens))
            metrics["cost_savings_usd"] += ((raw_tokens - reduced_tokens) / 1000.0) * self.cost_per_1k_tokens
            metrics["context_size_tokens"] += reduced_tokens
            
            # Answer consistency: does the reduced context yield the same answer as the raw context?
            baseline_ans = self.baseline_answers.get(doc_id, "")
            if not is_baseline and baseline_ans:
                metrics["answer_consistency"] += self.semantic_similarity(llm_answer, baseline_ans)
            
        num_items = max(1, len(dataset))
        return {k: round(float(v) / num_items, 4) for k, v in metrics.items()}

    def run(self, dataset: List[Dict], scre_instance):
        print("Running comprehensive baseline benchmark...")
        if not BM25Okapi or not self.embedder:
            print("Missing dependencies: rank_bm25 or sentence_transformers. Install to run benchmarks.")
            return {}
            
        # 1. Raw Context Strategy
        def raw_strategy(ctx, q): return ctx
        
        # 2. BM25 Strategy
        def bm25_strategy(ctx, q):
            sentences = ctx.split('.')
            tokenized_corpus = [doc.split() for doc in sentences]
            bm25 = BM25Okapi(tokenized_corpus)
            tokenized_query = q.split()
            top_docs = bm25.get_top_n(tokenized_query, sentences, n=4)
            return ". ".join(top_docs)
            
        # 3. Vector Strategy
        def vector_strategy(ctx, q):
            sentences = ctx.split('.')
            valid_sents = [s for s in sentences if len(s.strip()) > 5]
            if not valid_sents: return ctx
            corpus_embeddings = self.embedder.encode(valid_sents, convert_to_tensor=True)
            query_embedding = self.embedder.encode(q, convert_to_tensor=True)
            hits = util.semantic_search(query_embedding, corpus_embeddings)[0]
            top_hits = sorted(hits[:4], key=lambda x: x['corpus_id'])
            return ". ".join([valid_sents[hit['corpus_id']] for hit in top_hits])
            
        # 4. SCRE Strategy
        def scre_strategy(ctx, q):
            result = scre_instance.reduce(text=ctx, query=q, max_sentences=4)
            return result["context"]
            
        results = {
            "A_Raw_Context": self.evaluate_strategy("Raw", raw_strategy, dataset),
            "B_BM25": self.evaluate_strategy("BM25", bm25_strategy, dataset),
            "C_Vector_Search": self.evaluate_strategy("Vector", vector_strategy, dataset),
            "D_SCRE": self.evaluate_strategy("SCRE", scre_strategy, dataset)
        }
        
        print(json.dumps(results, indent=2))
        
        tracker = BenchmarkTrackerV2()
        tracker.record_run(results)
        print(f"\n📊 Benchmark report automatically updated: {tracker.report_file}")
        
        return results

